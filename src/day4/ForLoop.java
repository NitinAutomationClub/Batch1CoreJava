package day4;

public class ForLoop {

	public static void main(String[] args) {
		//initilization , Condition , increment/decrement on a single line
		
		for(int i=0;i<=10;i++)
		{
			System.out.println(i);
			
		}
	System.out.println("Reeverse Order");
		for(int j=10;j>=0;j--)
		{
			System.out.println(j);
		
		}
	}

}
